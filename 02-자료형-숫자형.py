#숫자를 ""안에 넣으면 문자열 되고 그냥 쓰면 숫자형이 된다.
#소수점 없으면 정수형, 있으면 실수형
num1 = 15
num2 = 20
print(num1 + num2)
print(num1 * num2)
print(num1 - num2)
print(num1 / num2) #나누기
print(num1 ** 3) #제곱
print(num1 % 3) #나누고 나머지
print(num1 // 3) #나누고 몫

#비교 연산자
print(num1 < num2)
print(num1 <= num2) #등호 기호가 뒤
print(num1 == num2) #두 값이 같은지 판단
print(num1 != num2) #두 값이 다른지 판단

